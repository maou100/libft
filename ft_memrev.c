#include "libft.h"

void	*ft_memrev(void *dst, const void *src, int n)
{
	int	i;
	int	j;

	j = 0;
	i = n - 1;
	while (i > -1)
	{
		((char *)dst)[j] = ((char *)src)[i];
		i--;
		j++;
	}
	return (dst);
}
